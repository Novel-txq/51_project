#include <REGX52.H>
#include "Delay.h"
#include "MatrixLED.h"

unsigned char Animation[]=
{
	0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,
	0x00,0x7C,0x10,0x10,0x10,0x7C,0x00,0x00,0x38,0x54,0x54,0x34,0x00,0x00,0x7C,0x04,
	0x00,0x7C,0x04,0x00,0x38,0x44,0x44,0x38,0x00,0x00,0x7A,0x00,0x00,0x00,0x00,0x00,
	0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00
};

void main()
{
	unsigned char i=0,offset=0,count = 0;
	MatrixLED_Init();
	while(1)
	{
		for(i=0;i<8;i++)//显示在屏幕上的8行8列
		{
			MatrixLED_ShowColumn(i,Animation[i+offset]);
		}
		count++;
		if(count>10)//一个延时操作
		{
			count = 0;
			offset++;
		}
		if(offset>40)
		{
			offset = 0;
		
		}
	}
}

